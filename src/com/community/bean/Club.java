package com.community.bean;

public class Club {
	
	private String id;
	private String name;
	private String intro;
	private String collegeId;
	private String generalId;
	private String presidentId;
	private String vice1;
	private String vice2;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	public String getCollegeId() {
		return collegeId;
	}
	public void setCollegeId(String collegeId) {
		this.collegeId = collegeId;
	}
	public String getGeneralId() {
		return generalId;
	}
	public void setGeneralId(String generalId) {
		this.generalId = generalId;
	}
	public String getPresidentId() {
		return presidentId;
	}
	public void setPresidentId(String presidentId) {
		this.presidentId = presidentId;
	}
	public String getVice1() {
		return vice1;
	}
	public void setVice1(String vice1) {
		this.vice1 = vice1;
	}
	public String getVice2() {
		return vice2;
	}
	public void setVice2(String vice2) {
		this.vice2 = vice2;
	}
	
	
}
